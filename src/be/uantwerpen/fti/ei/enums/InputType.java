package be.uantwerpen.fti.ei.enums;

/**
 * Enumeration for classifying a (keyboard) input type
 */
public enum InputType {
    /** Specifies that there is a Left (movement) input */
    LEFT,
    /** Specifies that there is a Right (movement) input */
    RIGHT,
    /** Specifies that there is an Upwards (movement) input */
    UP,
    /** Specifies that there is a Downwards (movement) input */
    DOWN,
    /** Specifies that there is a Space (shooting) input */
    SPACE,
    /** Specifies that there is an Escape (leave) input */
    ESCAPE,
    /** Specifies that there is an Enter (confirmation) input */
    ENTER
}
