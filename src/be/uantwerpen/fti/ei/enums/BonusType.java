package be.uantwerpen.fti.ei.enums;

/**
 * Enumeration for classifying a bonus type
 */
public enum BonusType {
    /** Specifies there is no bonus */
    NONE,
    /** Specifies that there is a boost in score */
    BOOST_SCORE,
    /** Specifies that an entity can temporarily use rockets */
    USE_ROCKET
}
